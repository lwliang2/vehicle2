const root = process.cwd();
let failures = 0;
let skipped = 0;

for (const source of SOURCES) {
  console.log(`\n=== ${source.label} ===`);
  try {
    const downloaded = await downloadAndExtractCsv(source.url, source.label);
    validate(downloaded);

    let baseline = readExistingCsv(root, source);
    if (!baseline) {
      console.log("No previous sync found — seeding from the app's existing fallback data...");
      try {
        baseline = await source.getSeed(root);
        if (baseline) {
          const rowCount = baseline.trim().split(/\r?\n/).length - 1;
          console.log(`Seeded with ${rowCount} row(s) from the fallback source.`);
        } else {
          console.log("Fallback source had nothing to seed with — using the download as-is.");
        }
      } catch (err) {
        console.warn(
          "Could not fetch seed data, proceeding with just the fresh download:",
          err instanceof Error ? err.message : err,
        );
        baseline = "";
      }
    }

    const mergeFn = source.mergeFn ?? mergeCsv;
    const { text: merged, changed } = mergeFn(baseline, downloaded, source, root);

    if (!changed) {
      console.log(
        `No safe changes to apply for ${source.label} — left ${source.outFile} untouched.`,
      );
      skipped++;
      continue;
    }

    const now = new Date().toISOString();
    const body =
      source.header +
      `export const ${source.updatedConst}: string | null = ${JSON.stringify(now)};\n` +
      `export const ${source.csvConst}: string = ${JSON.stringify(merged)};\n`;
    writeFileSync(join(root, source.outFile), body);
    console.log(`Wrote ${source.outFile}, updated ${now}`);
  } catch (err) {
    failures++;
    console.error(`FAILED: ${source.label}`);
    console.error(err instanceof Error ? err.message : err);
    // Deliberately don't touch the existing generated file on failure — a
    // stale-but-valid dataset beats overwriting with garbage or an empty file.
  }
}

if (skipped > 0) {
  console.warn(
    `\n${skipped} source(s) had a format mismatch and were left unchanged — check the ` +
      `warnings above. This does NOT fail the sync (no data was lost), but the LTA file's ` +
      `layout has drifted from what this script expects, so review src/data/*-live.ts.`,
  );
}
if (failures > 0) {
  console.error(`\n${failures} of ${SOURCES.length} source(s) failed to sync.`);
  process.exit(1);
}
console.log("\nAll sources synced successfully.");

async function downloadAndExtractCsv(url, label) {
  const res = await fetch(url, {
    headers: { "User-Agent": "coe-insights-sync/1.0 (+github actions monthly sync)" },
    signal: AbortSignal.timeout(60_000),
  });
  if (!res.ok) {
    throw new Error(`Download failed: ${res.status} ${res.statusText} — ${url}`);
  }
  const buf = new Uint8Array(await res.arrayBuffer());

  let entries;
  try {
    entries = unzipSync(buf);
  } catch (err) {
    throw new Error(
      `Could not unzip ${label} — the file at this URL may no longer be a zip, or the ` +
        `layout changed. Check ${url} manually. (${err instanceof Error ? err.message : err})`,
    );
  }

  const csvNames = Object.keys(entries).filter((name) => /\.csv$/i.test(name));
  if (csvNames.length === 0) {
    throw new Error(
      `No .csv file found inside the ${label} zip. Files present: ${Object.keys(entries).join(", ") || "(none)"}`,
    );
  }
  // If there are multiple CSVs, prefer the largest — smaller ones are
  // typically a readme/notes file bundled alongside the real dataset.
  csvNames.sort((a, b) => entries[b].length - entries[a].length);
  const chosen = csvNames[0];
  if (csvNames.length > 1) {
    console.log(`Multiple CSVs found (${csvNames.join(", ")}), using largest: ${chosen}`);
  }

  let text = strFromU8(entries[chosen]);
  // Strip a UTF-8 BOM if present — common in government-published CSVs and
  // would otherwise corrupt the first header name.
  if (text.charCodeAt(0) === 0xfeff) text = text.slice(1);
  return text.trim() + "\n";
}

function validate(csvText) {
  const lines = csvText.trim().split(/\r?\n/);
  if (lines.length < 2) {
    throw new Error(`Extracted CSV has fewer than 2 lines — looks empty or malformed.`);
  }
  console.log(`Header row: ${lines[0]}`);
  console.log(`${lines.length - 1} data row(s) in this download`);
}

// Reads src/data/vehicle-population-csv.ts's DEFAULT_VEHICLE_POPULATION_CSV
// template literal (a multi-line backtick string, not JSON-encoded like the
// generated *-live.ts files) so the first-ever sync can merge into it rather
// than starting from an empty dataset.
function readEmbeddedVehiclePopulationCsv(root) {
  const path = join(root, "src/data/vehicle-population-csv.ts");
  if (!existsSync(path)) return "";
  const contents = readFileSync(path, "utf8");
  const marker = "export const DEFAULT_VEHICLE_POPULATION_CSV = `";
  const start = contents.indexOf(marker);
  if (start < 0) return "";
  const afterMarker = contents.slice(start + marker.length);
  const end = afterMarker.lastIndexOf("`;");
  if (end < 0) return "";
  return afterMarker.slice(0, end);
}

// Fetches the same data.gov.sg COE dataset the app falls back to at request
// time (see src/lib/coe.functions.ts), converted to CSV text, so the
// first-ever sync can merge into it rather than starting from an empty
// dataset. Mirrors that file's pagination/field logic; kept independent so
// this plain .mjs script doesn't need to import app TypeScript.
async function fetchCoeFallbackCsv() {
  const resourceId = process.env.COE_RESOURCE_ID ?? "d_69b3380ad7e51aff3a7dcc84eba52b8a";
  const base = "https://data.gov.sg/api/action/datastore_search";
  const limit = 5000;
  let offset = 0;
  const rows = [];

  while (true) {
    const url = `${base}?resource_id=${resourceId}&limit=${limit}&offset=${offset}`;
    const res = await fetch(url, { signal: AbortSignal.timeout(30_000) });
    if (!res.ok) throw new Error(`data.gov.sg responded ${res.status}`);
    const json = await res.json();
    if (!json.success || !json.result)
      throw new Error("data.gov.sg returned an unexpected payload");
    const batch = json.result.records;
    rows.push(...batch);
    if (batch.length < limit || rows.length >= json.result.total) break;
    offset += limit;
    if (offset > 50000) break;
  }

  if (rows.length === 0) return "";
  const header = [
    "month",
    "bidding_no",
    "vehicle_class",
    "quota",
    "bids_success",
    "bids_received",
    "premium",
  ];
  const lines = [header.join(",")];
  for (const r of rows) {
    lines.push(header.map((col) => serialiseCell(String(r[col] ?? ""))).join(","));
  }
  return lines.join("\n") + "\n";
}

function serialiseCell(cell) {
  return /[",\n]/.test(cell) ? `"${cell.replace(/"/g, '""')}"` : cell;
}

// Reads the CSV text currently committed in src/data/*-live.ts, if any.
// These files are always a single `export const NAME: string = "...";` line
// (written by this same script via JSON.stringify), so we can pull it back
// out with a straightforward per-line scan rather than a full JS parser.
function readExistingCsv(root, source) {
  const path = join(root, source.outFile);
  if (!existsSync(path)) return "";
  const contents = readFileSync(path, "utf8");
  const prefix = `export const ${source.csvConst}: string = `;
  const line = contents.split("\n").find((l) => l.startsWith(prefix));
  if (!line) return "";
  const literal = line.slice(prefix.length).replace(/;$/, "");
  try {
    return JSON.parse(literal);
  } catch {
    console.warn(`Could not parse existing ${source.outFile} — treating as empty.`);
    return "";
  }
}

function parseCsvLine(line) {
  const out = [];
  let cur = "";
  let quoted = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (quoted) {
      if (ch === '"' && line[i + 1] === '"') {
        cur += '"';
        i++;
      } else if (ch === '"') {
        quoted = false;
      } else {
        cur += ch;
      }
    } else if (ch === '"') {
      quoted = true;
    } else if (ch === ",") {
      out.push(cur);
      cur = "";
    } else {
      cur += ch;
    }
  }
  out.push(cur);
  return out.map((s) => s.trim());
}

function parseCsvRows(text) {
  const lines = text
    .replace(/\r\n?/g, "\n")
    .split("\n")
    .filter((l) => l.trim().length > 0);
  if (lines.length === 0) return { header: [], rows: [] };
  return { header: parseCsvLine(lines[0]), rows: lines.slice(1).map(parseCsvLine) };
}

function normaliseHeader(h) {
  return h.toLowerCase().replace(/[^a-z0-9]/g, "");
}

function resolveKeyColumns(header, keyAliases) {
  const normalised = header.map(normaliseHeader);
  const resolved = [];
  for (const { field, aliases, required } of keyAliases) {
    const idx = normalised.findIndex((h) => aliases.includes(h));
    if (idx < 0) {
      if (required) return null; // can't build a key without this column
      continue;
    }
    resolved.push({ field, idx });
  }
  return resolved;
}

function buildKeyFn(header, keyAliases) {
  const cols = resolveKeyColumns(header, keyAliases);
  if (!cols) return null;
  return (row) => cols.map(({ idx }) => (row[idx] ?? "").trim().toLowerCase()).join("|");
}

// Merges `newText` (freshly downloaded) with `oldText` (currently committed).
// New rows win on key collisions; old rows whose key isn't present in the
// new download are preserved. If headers don't line up cleanly enough to
// build a safe key, this KEEPS the old data untouched rather than replacing
// it with the new download — losing history to a format hiccup is worse
// than being one sync cycle behind. Returns { text, changed } so the caller
// can tell "nothing to commit" apart from "actually merged."
function mergeCsv(oldText, newText, source) {
  if (!oldText) return { text: newText, changed: true };

  const oldParsed = parseCsvRows(oldText);
  const newParsed = parseCsvRows(newText);
  if (oldParsed.rows.length === 0) return { text: newText, changed: true };

  if (normaliseHeader(oldParsed.header.join(",")) !== normaliseHeader(newParsed.header.join(","))) {
    console.warn(
      `Header layout changed since the last sync for ${source.label} — ` +
        `merging by exact column position isn't safe. KEEPING existing data ` +
        `unchanged rather than risking data loss; this download is skipped ` +
        `until the format mismatch is investigated. ` +
        `(old: "${oldParsed.header.join(",")}" vs new: "${newParsed.header.join(",")}")`,
    );
    return { text: oldText, changed: false };
  }

  const keyFn = buildKeyFn(newParsed.header, source.keyAliases);
  if (!keyFn) {
    console.warn(
      `Could not identify key column(s) for ${source.label} (looked for: ` +
        `${source.keyAliases.map((k) => k.field).join(", ")}). KEEPING existing ` +
        `data unchanged rather than risking data loss.`,
    );
    return { text: oldText, changed: false };
  }

  const merged = new Map();
  for (const row of oldParsed.rows) merged.set(keyFn(row), row);
  const oldCount = merged.size;
  for (const row of newParsed.rows) merged.set(keyFn(row), row); // new wins
  const addedOrUpdated = newParsed.rows.length;
  const keptFromOld = merged.size - addedOrUpdated >= 0 ? merged.size - addedOrUpdated : 0;

  console.log(
    `Merge: ${oldCount} existing row(s) + ${newParsed.rows.length} downloaded row(s) ` +
      `→ ${merged.size} total (${keptFromOld} kept only from history)`,
  );

  const rows = [...merged.values()];
  const text = [newParsed.header.join(","), ...rows.map(serialiseRow)].join("\n") + "\n";
  return { text, changed: true };
}

function serialiseRow(cells) {
  return cells.map(serialiseCell).join(",");
}

// Reads just the header row of the embedded snapshot — this is the
// canonical list of known category column names/order, used both to
// recognise columns in a wide-format download and to match category/type
// names in a long-format download (LTA's "type" values are usually the
// exact same strings as these column names).
function readCanonicalColumns(root) {
  const csv = readEmbeddedVehiclePopulationCsv(root);
  if (!csv) return [];
  const [headerLine] = csv.trim().split(/\r?\n/);
  return parseCsvLine(headerLine).slice(1); // drop "Year"
}

// Normalises either shape into Map<year, Map<columnName, value>>:
//   - wide:  "Year,Private Cars,..."      → one row per year
//   - long:  "year,category,type,number"  → one row per (year, category/type)
// Matching against `knownColumns` is case-insensitive; unmatched
// category/type values are logged and dropped (same limitation the app's
// own long-format parser already has).
function normaliseVehiclePopulationCsv(text, knownColumns, label) {
  const { header, rows } = parseCsvRows(text);
  if (header.length === 0) return { byYear: new Map(), unmatched: [] };

  const normHeader = header.map(normaliseHeader);
  const knownByNorm = new Map(knownColumns.map((c) => [normaliseHeader(c), c]));
  const isWide = normHeader[0] === "year" && header.length > 4;

  const byYear = new Map();
  const unmatched = new Set();

  if (isWide) {
    for (const row of rows) {
      const year = row[0];
      if (!year) continue;
      let values = byYear.get(year);
      if (!values) byYear.set(year, (values = new Map()));
      for (let i = 1; i < header.length; i++) {
        const col = knownByNorm.get(normHeader[i]) ?? header[i]; // keep unknown columns too, don't silently drop them
        const raw = (row[i] ?? "").replace(/,/g, "").trim();
        if (raw === "" || raw === "-") continue;
        const num = Number(raw);
        if (Number.isFinite(num)) values.set(col, num);
      }
    }
  } else {
    // long format: year, category, type, number (column order by position,
    // matching the app's own parseLong — LTA doesn't always label them
    // exactly "category"/"type"/"number")
    for (const row of rows) {
      if (row.length < 4) continue;
      const [year, category, type, numberRaw] = row;
      if (!year) continue;
      const num = Number((numberRaw ?? "").replace(/,/g, "").trim());
      if (!Number.isFinite(num)) continue;

      const candidate = [type, category].find((c) => c && knownByNorm.has(normaliseHeader(c)));
      const col = candidate ? knownByNorm.get(normaliseHeader(candidate)) : null;
      if (!col) {
        unmatched.add(`${category} / ${type}`);
        continue;
      }
      let values = byYear.get(year);
      if (!values) byYear.set(year, (values = new Map()));
      values.set(col, (values.get(col) ?? 0) + num); // sum, matching the app's parseLong behaviour
    }
  }

  if (unmatched.size > 0) {
    console.warn(
      `${label}: ${unmatched.size} category/type value(s) didn't match any known column and were skipped: ` +
        [...unmatched].slice(0, 10).join(", ") +
        (unmatched.size > 10 ? ", …" : ""),
    );
  }

  return { byYear, format: isWide ? "wide" : "long" };
}

// Vehicle-population-specific merge: normalises old + new (regardless of
// which of the two shapes each one is in) into a common year→column→value
// structure, merges by year (new values override old per year+column; years
// or columns not touched by the new download are preserved), then always
// re-serialises as wide format using a stable column order — so every
// future sync compares against a consistent, self-produced baseline instead
// of whatever shape LTA happened to publish that month.
function mergeVehiclePopulationCsv(oldText, newText, source, root) {
  const knownColumns = readCanonicalColumns(root);
  if (knownColumns.length === 0) {
    console.warn(
      `Could not read the embedded column list for ${source.label} — falling back to the generic merge.`,
    );
    return mergeCsv(oldText, newText, source);
  }

  const { byYear: newByYear, format: newFormat } = normaliseVehiclePopulationCsv(
    newText,
    knownColumns,
    "new download",
  );
  if (newByYear.size === 0) {
    throw new Error("Downloaded vehicle population CSV had no rows this script could parse.");
  }

  let oldByYear = new Map();
  if (oldText) {
    ({ byYear: oldByYear } = normaliseVehiclePopulationCsv(oldText, knownColumns, "existing data"));
  }

  const oldYearCount = oldByYear.size;
  const merged = new Map(oldByYear);
  for (const [year, values] of newByYear) {
    const existing = merged.get(year) ?? new Map();
    merged.set(year, new Map([...existing, ...values])); // new columns win per year
  }

  const keptFromOld = [...merged.keys()].filter((y) => !newByYear.has(y)).length;
  console.log(
    `Merge (${newFormat} download): ${oldYearCount} existing year(s) + ${newByYear.size} ` +
      `year(s) in download → ${merged.size} total (${keptFromOld} kept only from history)`,
  );

  const years = [...merged.keys()].sort((a, b) => Number(a) - Number(b));
  const header = ["Year", ...knownColumns];
  const rows = years.map((year) => {
    const values = merged.get(year);
    return [year, ...knownColumns.map((col) => (values.has(col) ? String(values.get(col)) : ""))];
  });
  const text = [header.join(","), ...rows.map(serialiseRow)].join("\n") + "\n";
  return { text, changed: true };
}
